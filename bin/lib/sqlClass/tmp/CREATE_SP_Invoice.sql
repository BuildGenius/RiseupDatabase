CREATE PROCEDURE Incremental_push_Invoice
AS
IF (EXISTS (
	SELECT * FROM INFORMATION_SCHEMA.TABLES
	WHERE TABLE_CATALOG = '[min-project]' 
	AND TABLE_SCHEMA = 'dbo' 
	AND TABLE_TYPE = 'BASE TABLE' 
	AND TABLE_NAME = 'Invoice'
) ) 
BEGIN 
	INSERT INTO Invoice (
		[TransactionID],
	[SellID],
	[SellBranch],
	[Product],
	[Serial],
	[InvoiceDate],
	[CusCode],
	[CreditDay],
	[CurrencyCode],
	[ExchangeRate],
	[CustomerName],
	[MemberName],
	[TaxInvoice],
	[RowNo],
	[Number],
	[UnitPrice],
	[TotalPriceBV],
	[TotalPriceAV],
	[VATAmount],
	[VATRate],
	[VATType],
	[Status],
	[CrTime],
	[UTime],
	[ImportTime],
	[BPC_status],
	[BPC_Remark],
	[BPC_StatusDatetime]
	) SELECT 
		[TransactionID],
	[SellID],
	[SellBranch],
	[Product],
	[Serial],
	[InvoiceDate],
	[CusCode],
	[CreditDay],
	[CurrencyCode],
	[ExchangeRate],
	[CustomerName],
	[MemberName],
	[TaxInvoice],
	[RowNo],
	[Number],
	[UnitPrice],
	[TotalPriceBV],
	[TotalPriceAV],
	[VATAmount],
	[VATRate],
	[VATType],
	[Status],
	[CrTime],
	[UTime],
	[ImportTime],
	[BPC_status],
	[BPC_Remark],
	[BPC_StatusDatetime]
	FROM [43.254.133.155].[ITECToAX].[dbo].Invoice
	WHERE UTime > (SELECT MAX(UTime) FROM [dbo].Invoice)
END
ELSE BEGIN 
	CREATE TABLE Invoice (
		[TransactionID] int,
	[SellID] int,
	[SellBranch] int,
	[Product] varchar(40),
	[Serial] varchar(40),
	[InvoiceDate] datetime,
	[CusCode] varchar(20),
	[CreditDay] int,
	[CurrencyCode] varchar(50),
	[ExchangeRate] money,
	[CustomerName] varchar(300),
	[MemberName] varchar(300),
	[TaxInvoice] varchar(30),
	[RowNo] int,
	[Number] int,
	[UnitPrice] money,
	[TotalPriceBV] money,
	[TotalPriceAV] money,
	[VATAmount] money,
	[VATRate] money,
	[VATType] int,
	[Status] int,
	[CrTime] datetime,
	[UTime] datetime,
	[ImportTime] datetime,
	[BPC_status] int,
	[BPC_Remark] varchar(MAX),
	[BPC_StatusDatetime] datetime
	)
	
	INSERT INTO Invoice (
		[TransactionID],
	[SellID],
	[SellBranch],
	[Product],
	[Serial],
	[InvoiceDate],
	[CusCode],
	[CreditDay],
	[CurrencyCode],
	[ExchangeRate],
	[CustomerName],
	[MemberName],
	[TaxInvoice],
	[RowNo],
	[Number],
	[UnitPrice],
	[TotalPriceBV],
	[TotalPriceAV],
	[VATAmount],
	[VATRate],
	[VATType],
	[Status],
	[CrTime],
	[UTime],
	[ImportTime],
	[BPC_status],
	[BPC_Remark],
	[BPC_StatusDatetime]
	) 
	SELECT 
        [TransactionID],
	[SellID],
	[SellBranch],
	[Product],
	[Serial],
	[InvoiceDate],
	[CusCode],
	[CreditDay],
	[CurrencyCode],
	[ExchangeRate],
	[CustomerName],
	[MemberName],
	[TaxInvoice],
	[RowNo],
	[Number],
	[UnitPrice],
	[TotalPriceBV],
	[TotalPriceAV],
	[VATAmount],
	[VATRate],
	[VATType],
	[Status],
	[CrTime],
	[UTime],
	[ImportTime],
	[BPC_status],
	[BPC_Remark],
	[BPC_StatusDatetime]
	FROM [43.254.133.155].[ITECToAX].[dbo].Invoice
END